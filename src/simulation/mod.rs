/// Simulation field data
pub struct SimulationField {
    /// Field name
    pub name: String,
    /// Field values
    pub values: Vec<f64>,
    /// Field type
    pub field_type: FieldType,
}

/// Field types
pub enum FieldType {
    /// Scalar field (e.g., temperature, pressure)
    Scalar,
    /// Vector field (e.g., velocity, displacement)
    Vector,
    /// Tensor field (e.g., stress, strain)
    Tensor,
}

impl SimulationField {
    /// Create a new scalar field
    pub fn new_scalar(name: &str, values: Vec<f64>) -> Self {
        Self {
            name: name.to_string(),
            values,
            field_type: FieldType::Scalar,
        }
    }

    /// Create a new vector field
    pub fn new_vector(name: &str, values: Vec<f64>) -> Self {
        Self {
            name: name.to_string(),
            values,
            field_type: FieldType::Vector,
        }
    }

    /// Create a new tensor field
    pub fn new_tensor(name: &str, values: Vec<f64>) -> Self {
        Self {
            name: name.to_string(),
            values,
            field_type: FieldType::Tensor,
        }
    }
}
