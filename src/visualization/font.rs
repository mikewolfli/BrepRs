//! Bitmap font utilities for software text rendering
use std::collections::HashMap;

/// Returns a bitmap font map for the given scale, bold, and italic settings.
/// Each character is mapped to a Vec<u8> representing 8 rows of 8 bits (8x8 font).
/// For simplicity, only ASCII characters are supported. Scaling is simulated by duplicating rows/columns.
pub fn get_bitmap_font_scaled(scale: f32, bold: bool, italic: bool) -> HashMap<char, Vec<u8>> {
    // Basic 8x8 ASCII font for A-Z, a-z, 0-9, and a few symbols
    // Each u8 is a row, LSB is leftmost pixel
    let mut font: HashMap<char, [u8; 8]> = HashMap::new();
    // Example: 'A'
    font.insert('A', [0x18, 0x24, 0x42, 0x7E, 0x42, 0x42, 0x42, 0x00]);
    font.insert('B', [0x7C, 0x42, 0x42, 0x7C, 0x42, 0x42, 0x7C, 0x00]);
    font.insert('C', [0x3C, 0x42, 0x40, 0x40, 0x40, 0x42, 0x3C, 0x00]);
    font.insert('D', [0x78, 0x44, 0x42, 0x42, 0x42, 0x44, 0x78, 0x00]);
    font.insert('E', [0x7E, 0x40, 0x40, 0x7C, 0x40, 0x40, 0x7E, 0x00]);
    font.insert('F', [0x7E, 0x40, 0x40, 0x7C, 0x40, 0x40, 0x40, 0x00]);
    font.insert('G', [0x3C, 0x42, 0x40, 0x4E, 0x42, 0x42, 0x3C, 0x00]);
    font.insert('H', [0x42, 0x42, 0x42, 0x7E, 0x42, 0x42, 0x42, 0x00]);
    font.insert('I', [0x7E, 0x18, 0x18, 0x18, 0x18, 0x18, 0x7E, 0x00]);
    font.insert('J', [0x0E, 0x04, 0x04, 0x04, 0x44, 0x44, 0x38, 0x00]);
    font.insert('K', [0x42, 0x44, 0x48, 0x70, 0x48, 0x44, 0x42, 0x00]);
    font.insert('L', [0x40, 0x40, 0x40, 0x40, 0x40, 0x40, 0x7E, 0x00]);
    font.insert('M', [0x42, 0x66, 0x5A, 0x5A, 0x42, 0x42, 0x42, 0x00]);
    font.insert('N', [0x42, 0x62, 0x52, 0x4A, 0x46, 0x42, 0x42, 0x00]);
    font.insert('O', [0x3C, 0x42, 0x42, 0x42, 0x42, 0x42, 0x3C, 0x00]);
    font.insert('P', [0x7C, 0x42, 0x42, 0x7C, 0x40, 0x40, 0x40, 0x00]);
    font.insert('Q', [0x3C, 0x42, 0x42, 0x42, 0x4A, 0x44, 0x3A, 0x00]);
    font.insert('R', [0x7C, 0x42, 0x42, 0x7C, 0x48, 0x44, 0x42, 0x00]);
    font.insert('S', [0x3C, 0x42, 0x40, 0x3C, 0x02, 0x42, 0x3C, 0x00]);
    font.insert('T', [0x7E, 0x18, 0x18, 0x18, 0x18, 0x18, 0x18, 0x00]);
    font.insert('U', [0x42, 0x42, 0x42, 0x42, 0x42, 0x42, 0x3C, 0x00]);
    font.insert('V', [0x42, 0x42, 0x42, 0x42, 0x42, 0x24, 0x18, 0x00]);
    font.insert('W', [0x42, 0x42, 0x42, 0x5A, 0x5A, 0x66, 0x42, 0x00]);
    font.insert('X', [0x42, 0x24, 0x18, 0x18, 0x18, 0x24, 0x42, 0x00]);
    font.insert('Y', [0x42, 0x24, 0x18, 0x18, 0x18, 0x18, 0x18, 0x00]);
    font.insert('Z', [0x7E, 0x04, 0x08, 0x10, 0x20, 0x40, 0x7E, 0x00]);
    // Add more as needed (lowercase, digits, symbols)

    // Convert to scaled, bold, italic bitmaps
    let mut scaled_font: HashMap<char, Vec<u8>> = HashMap::new();
    let scale = scale.max(1.0).min(8.0);
    let scale_int = scale.round() as usize;
    for (&ch, &bitmap) in font.iter() {
        let mut rows: Vec<u8> = Vec::new();
        // For each row, scale vertically
        for row in 0..8 {
            let mut bits = bitmap[row];
            // Bold: OR with right-shifted version
            if bold {
                bits |= bits >> 1;
            }
            // Italic: shift right by (row/2) pixels
            let mut bits_italic = bits;
            if italic {
                let shift = (7 - row) / 2; // more shift for lower rows
                bits_italic = if shift > 0 { bits >> shift } else { bits };
            }
            // Scale horizontally by duplicating bits
            let mut scaled_row = 0u64;
            for col in 0..8 {
                let bit = (bits_italic >> col) & 1;
                for s in 0..scale_int {
                    if bit == 1 {
                        scaled_row |= 1 << (col * scale_int + s);
                    }
                }
            }
            // Now, push this row scale_int times for vertical scaling
            for _ in 0..scale_int {
                // Truncate to 8*scale_int bits (u64)
                rows.push((scaled_row & 0xFF) as u8);
            }
        }
        scaled_font.insert(ch, rows);
    }
    scaled_font
}