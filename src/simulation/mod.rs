    /// Export mesh to FEAP format
    pub fn export_to_feap(&self, mesh: &Mesh3D, file_path: &str) -> Result<(), std::io::Error> {
        let mut file = File::create(file_path)?;

        // Write FEAP header
        writeln!(file, "FEAP input file generated by BrepRs")?;
        writeln!(file, "$")?;

        // Write nodes
        writeln!(file, "NODES")?;
        for (i, vertex) in mesh.vertices.iter().enumerate() {
            writeln!(
                file,
                "{:6} {:12.6} {:12.6} {:12.6}",
                i + 1,
                vertex.point.x,
                vertex.point.y,
                vertex.point.z
            )?;
        }
        writeln!(file, "$")?;

        // Write elements
        writeln!(file, "ELEMENTS")?;

        // Write tetrahedrons
        for (i, tetra) in mesh.tetrahedrons.iter().enumerate() {
            writeln!(
                file,
                "{:6} 4 {:6} {:6} {:6} {:6}",
                i + 1,
                tetra.vertices[0] + 1,
                tetra.vertices[1] + 1,
                tetra.vertices[2] + 1,
                tetra.vertices[3] + 1
            )?;
        }

        // Write hexahedrons
        for (i, hex) in mesh.hexahedrons.iter().enumerate() {
            writeln!(
                file,
                "{:6} 8 {:6} {:6} {:6} {:6} {:6} {:6} {:6} {:6}",
                i + mesh.tetrahedrons.len() + 1,
                hex.vertices[0] + 1,
                hex.vertices[1] + 1,
                hex.vertices[2] + 1,
                hex.vertices[3] + 1,
                hex.vertices[4] + 1,
                hex.vertices[5] + 1,
                hex.vertices[6] + 1,
                hex.vertices[7] + 1
            )?;
        }

        // Write prisms
        for (i, prism) in mesh.prisms.iter().enumerate() {
            writeln!(
                file,
                "{:6} 6 {:6} {:6} {:6} {:6} {:6} {:6}",
                i + mesh.tetrahedrons.len() + mesh.hexahedrons.len() + 1,
                prism.vertices[0] + 1,
                prism.vertices[1] + 1,
                prism.vertices[2] + 1,
                prism.vertices[3] + 1,
                prism.vertices[4] + 1,
                prism.vertices[5] + 1
            )?;
        }
        writeln!(file, "$")?;

        Ok(())
    }

    /// Export mesh to CalculiX format
    pub fn export_to_calculix(&self, mesh: &Mesh3D, file_path: &str) -> Result<(), std::io::Error> {
        let mut file = File::create(file_path)?;

        // Write CalculiX header
        writeln!(file, "*HEADING")?;
        writeln!(file, "CalculiX input file generated by BrepRs")?;
        writeln!(file, "*NODE")?;

        // Write nodes
        for (i, vertex) in mesh.vertices.iter().enumerate() {
            writeln!(
                file,
                "{:6}, {:12.6}, {:12.6}, {:12.6}",
                i + 1,
                vertex.point.x,
                vertex.point.y,
                vertex.point.z
            )?;
        }

        // Write tetrahedrons
        if !mesh.tetrahedrons.is_empty() {
            writeln!(file, "*ELEMENT, TYPE=C3D4")?;
            for (i, tetra) in mesh.tetrahedrons.iter().enumerate() {
                writeln!(
                    file,
                    "{:6}, {:6}, {:6}, {:6}, {:6}",
                    i + 1,
                    tetra.vertices[0] + 1,
                    tetra.vertices[1] + 1,
                    tetra.vertices[2] + 1,
                    tetra.vertices[3] + 1
                )?;
            }
        }

        // Write hexahedrons
        if !mesh.hexahedrons.is_empty() {
            writeln!(file, "*ELEMENT, TYPE=C3D8")?;
            for (i, hex) in mesh.hexahedrons.iter().enumerate() {
                writeln!(
                    file,
                    "{:6}, {:6}, {:6}, {:6}, {:6}, {:6}, {:6}, {:6}, {:6}",
                    i + mesh.tetrahedrons.len() + 1,
                    hex.vertices[0] + 1,
                    hex.vertices[1] + 1,
                    hex.vertices[2] + 1,
                    hex.vertices[3] + 1,
                    hex.vertices[4] + 1,
                    hex.vertices[5] + 1,
                    hex.vertices[6] + 1,
                    hex.vertices[7] + 1
                )?;
            }
        }

        // Write prisms
        if !mesh.prisms.is_empty() {
            writeln!(file, "*ELEMENT, TYPE=C3D6")?;
            for (i, prism) in mesh.prisms.iter().enumerate() {
                writeln!(
                    file,
                    "{:6}, {:6}, {:6}, {:6}, {:6}, {:6}, {:6}",
                    i + mesh.tetrahedrons.len() + mesh.hexahedrons.len() + 1,
                    prism.vertices[0] + 1,
                    prism.vertices[1] + 1,
                    prism.vertices[2] + 1,
                    prism.vertices[3] + 1,
                    prism.vertices[4] + 1,
                    prism.vertices[5] + 1
                )?;
            }
        }

        Ok(())
    }

    /// Export mesh to OpenFOAM format
    pub fn export_to_openfoam(&self, mesh: &Mesh3D, directory: &str) -> Result<(), std::io::Error> {
        // Create directory if it doesn't exist
        std::fs::create_dir_all(directory)?;

        // Write points
        let mut points_file = File::create(format!("{}/points", directory))?;
        writeln!(points_file, "{}", mesh.vertices.len())?;
        writeln!(points_file, "(")?;
        for vertex in &mesh.vertices {
            writeln!(
                points_file,
                "({} {} {})",
                vertex.point.x, vertex.point.y, vertex.point.z
            )?;
        }
        writeln!(points_file, ")")?;

        // Write faces
        let mut faces_file = File::create(format!("{}/faces", directory))?;
        let total_faces = mesh.faces.len()
            + mesh.tetrahedrons.len() * 4
            + mesh.hexahedrons.len() * 6
            + mesh.prisms.len() * 5;
        writeln!(faces_file, "{}", total_faces)?;
        writeln!(faces_file, "(")?;

        // Write regular faces
        for face in &mesh.faces {
            write!(faces_file, "({}", face.vertices.len())?;
            for &v in &face.vertices {
                write!(faces_file, " {}", v)?;
            }
            writeln!(faces_file, ")")?;
        }

        // Write tetrahedron faces
        for tetra in &mesh.tetrahedrons {
            let faces = [
                [tetra.vertices[0], tetra.vertices[1], tetra.vertices[2]],
                [tetra.vertices[0], tetra.vertices[2], tetra.vertices[3]],
                [tetra.vertices[0], tetra.vertices[3], tetra.vertices[1]],
                [tetra.vertices[1], tetra.vertices[3], tetra.vertices[2]],
            ];

            for face in &faces {
                writeln!(faces_file, "(3 {} {} {})", face[0], face[1], face[2])?;
            }
        }

        // Write hexahedron faces
        for hex in &mesh.hexahedrons {
            let faces = [
                [
                    hex.vertices[0],
                    hex.vertices[1],
                    hex.vertices[2],
                    hex.vertices[3],
                ],
                [
                    hex.vertices[4],
                    hex.vertices[5],
                    hex.vertices[6],
                    hex.vertices[7],
                ],
                [
                    hex.vertices[0],
                    hex.vertices[1],
                    hex.vertices[5],
                    hex.vertices[4],
                ],
                [
                    hex.vertices[1],
                    hex.vertices[2],
                    hex.vertices[6],
                    hex.vertices[5],
                ],
                [
                    hex.vertices[2],
                    hex.vertices[3],
                    hex.vertices[7],
                    hex.vertices[6],
                ],
                [
                    hex.vertices[3],
                    hex.vertices[0],
                    hex.vertices[4],
                    hex.vertices[7],
                ],
            ];

            for face in &faces {
                writeln!(
                    faces_file,
                    "(4 {} {} {} {})",
                    face[0], face[1], face[2], face[3]
                )?;
            }
        }

        // Write prism faces
        for prism in &mesh.prisms {
            let faces: Vec<Vec<usize>> = vec![
                vec![prism.vertices[0], prism.vertices[1], prism.vertices[2]],
                vec![prism.vertices[3], prism.vertices[4], prism.vertices[5]],
                vec![prism.vertices[0], prism.vertices[1], prism.vertices[4], prism.vertices[3]],
                vec![prism.vertices[1], prism.vertices[2], prism.vertices[5], prism.vertices[4]],
                vec![prism.vertices[2], prism.vertices[0], prism.vertices[3], prism.vertices[5]],
            ];

            for face in &faces {
                match face.len() {
                    3 => writeln!(faces_file, "(3 {} {} {})", face[0], face[1], face[2])?,
                    4 => writeln!(faces_file, "(4 {} {} {} {})", face[0], face[1], face[2], face[3])?,
                    _ => writeln!(faces_file, "({} {:?})", face.len(), face)?,
                }
            }
        }

        writeln!(faces_file, ")")?;

        // Write owner and neighbour files (placeholder)
        let mut owner_file = File::create(format!("{}/owner", directory))?;
        let mut neighbour_file = File::create(format!("{}/neighbour", directory))?;

        for _ in 0..total_faces {
            writeln!(owner_file, "0")?;
            writeln!(neighbour_file, "-1")?;
        }

        Ok(())
    }

    /// Export mesh to SU2 format
    pub fn export_to_su2(&self, mesh: &Mesh3D, file_path: &str) -> Result<(), std::io::Error> {
        let mut file = File::create(file_path)?;

        // Write SU2 header
        writeln!(file, "% SU2 mesh file generated by BrepRs")?;
        writeln!(file, "NDIME=3")?;
        writeln!(
            file,
            "NELEM={}",
            mesh.tetrahedrons.len() + mesh.hexahedrons.len() + mesh.prisms.len()
        )?;
        writeln!(file, "NPOIN={}", mesh.vertices.len())?;
        writeln!(file, "NMARK=0")?;
        writeln!(file, "NELEM_SURF=0")?;

        // Write elements
        for (i, tetra) in mesh.tetrahedrons.iter().enumerate() {
            writeln!(
                file,
                "{} 0 {} {} {} {}",
                i + 1,
                tetra.vertices[0] + 1,
                tetra.vertices[1] + 1,
                tetra.vertices[2] + 1,
                tetra.vertices[3] + 1
            )?;
        }

        for (i, hex) in mesh.hexahedrons.iter().enumerate() {
            writeln!(
                file,
                "{} 1 {} {} {} {} {} {} {} {}",
                i + mesh.tetrahedrons.len() + 1,
                hex.vertices[0] + 1,
                hex.vertices[1] + 1,
                hex.vertices[2] + 1,
                hex.vertices[3] + 1,
                hex.vertices[4] + 1,
                hex.vertices[5] + 1,
                hex.vertices[6] + 1,
                hex.vertices[7] + 1
            )?;
        }

        for (i, prism) in mesh.prisms.iter().enumerate() {
            writeln!(
                file,
                "{} 2 {} {} {} {} {} {}",
                i + mesh.tetrahedrons.len() + mesh.hexahedrons.len() + 1,
                prism.vertices[0] + 1,
                prism.vertices[1] + 1,
                prism.vertices[2] + 1,
                prism.vertices[3] + 1,
                prism.vertices[4] + 1,
                prism.vertices[5] + 1
            )?;
        }

        // Write nodes
        for (i, vertex) in mesh.vertices.iter().enumerate() {
            writeln!(
                file,
                "{} {} {} {}",
                i + 1,
                vertex.point.x,
                vertex.point.y,
                vertex.point.z
            )?;
        }

        Ok(())
    }

